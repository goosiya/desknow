# 한국어 요일 라벨(date.weekday() 0=월 … 6=일).
_WEEKDAYS_KO = ("월", "화", "수", "목", "금", "토", "일")


def build_system_prompt(now: datetime | None = None) -> str:
    """페르소나 프롬프트에 **현재 KST 날짜 맥락**을 덧붙여 반환한다(매 모델 호출 시 동적 생성).

    ★왜 필요한가(KTH 2026-06-18 버그): LLM은 학습 시점 이후의 '오늘'을 알 수 없어, 사용자가
    "오늘"·"내일"·"19일"처럼 상대/부분 날짜를 말하면 연·월을 멋대로(대개 과거로) 추측해 잘못된
    ISO 날짜를 ``search_available_rooms``에 넘긴다. 과거 날짜는 슬롯 도출에서 ``slot_start >= now``
    필터에 전부 걸려 "빈 방 0건"으로 오답이 났다. 그래서 매 호출마다 오늘 날짜(KST)를 주입해
    상대/부분 날짜를 정확한 연·월·일로 환산하게 한다.

    상태에 영속시키지 않고 호출 시점에 생성하므로(graph.call_model), 날짜가 바뀌어도 과거 대화에
    옛 날짜가 박제되지 않는다. ``now``는 테스트 결정성을 위해 주입받으며 미지정 시 ``now_utc()``.
    """
    current = now if now is not None else now_utc()
    today = today_in_tz(ROOM_TZ, now=current)  # KST 기준 '오늘' 날짜
    tomorrow = today + timedelta(days=1)
    weekday = _WEEKDAYS_KO[today.weekday()]
    date_context = (
        f"\n\n참고로 오늘은 {today.year}년 {today.month}월 {today.day}일 {weekday}요일이에요"
        "(한국 시간 KST 기준). 사용자가 '오늘'·'내일'·'모레'·'이번 주말'·'19일'처럼 상대적이거나 "
        "일부만 말한 날짜는 **이 오늘 날짜를 기준으로** 정확한 연·월·일로 환산해요. "
        "search_available_rooms의 date 인자에는 반드시 환산한 ISO 날짜(YYYY-MM-DD)를 넣어요"
        f"(예: '내일' → {tomorrow.isoformat()})."
    )
    return ROOMMATE_SYSTEM_PROMPT + date_context
